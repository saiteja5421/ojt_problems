# set
# Set does not store duplicate values and it is unorder no indexing


s = {1, 2, 2, 3, 4, 5}
print(s)
s.add(4)
s.discard(5)
print(s)

# dict
d = {1: "sai", 2: "teja", 3: "reddy"}
print(d)
d[2] = "saiteja"
d[4] = "fun"
print(d)
